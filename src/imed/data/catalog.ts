// დამხმარე: კატეგორიის ServiceType-ის დადგენა ქვეკატეგორიის key-დან
export function findCategoryByKey(catKey: string): CatalogCategory | undefined {
  return CATALOG.find(c => c.key === catKey);
}

// ბრტყელი ძებნა მთელ კატალოგში
export interface FlatCatalogItem extends CatalogItem {
  categoryKey: string;
  categoryLabel: string;
  type: ServiceType;
  subKey: string;
  subLabel: string;
}

export function flattenCatalog(): FlatCatalogItem[] {
  const out: FlatCatalogItem[] = [];
  for (const cat of CATALOG) {
    for (const sub of cat.subcategories) {
      for (const item of sub.items) {
        out.push({
          ...item,
          categoryKey: cat.key,
          categoryLabel: cat.label,
          type: cat.type,
          subKey: sub.key,
          subLabel: sub.label,
        });
      }
    }
  }
  return out;
}

export function searchCatalog(term: string, limitN = 50): FlatCatalogItem[] {
  const t = term.toLowerCase().trim();
  if (!t) return [];
  return flattenCatalog()
    .filter(i => i.name.toLowerCase().includes(t) || (i.code && i.code.toLowerCase().includes(t)))
    .slice(0, limitN);
}
